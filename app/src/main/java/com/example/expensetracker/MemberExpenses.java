package com.example.expensetracker;

public class MemberExpenses {
    private String memberExpenses;
    private String memberDes;
    private String memberDate;

    public MemberExpenses() {
    }

    public String getMemberExpenses() {
        return memberExpenses;
    }

    public void setMemberExpenses(String memberExpenses) {
        this.memberExpenses = memberExpenses;
    }

    public String getMemberDes() {
        return memberDes;
    }

    public void setMemberDes(String memberDes) {
        this.memberDes = memberDes;
    }

    public String getMemberDate() {
        return memberDate;
    }

    public void setMemberDate(String memberDate) {
        this.memberDate = memberDate;
    }
}
